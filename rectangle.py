
def area(h, w):
    '''
    Returns area of given rectangle.
    
    Arguments:
        h (float): rectangle height
		w (float): rectangle width
    
    Return value:
		area(h, w): rectangle area
    '''
    return h * w

def perimeter(h, w):
    '''
    Returns perimeter of given rectangle.
    
    Arguments:
        h (float): rectangle height
		w (float): rectangle width
    
    Return value:
		area(h, w): rectangle perimeter
    '''
    return (h + w) * 2
