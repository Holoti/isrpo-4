
def area(a):
    '''
    Returns area of given square.
    
    Arguments:
		a (float): square side
    
    Return value:
		area(a): square area
    '''
    return a * a

def perimeter(a):
    '''
    Returns perimeter of given square.
    
    Arguments:
		a (float): square side
    
    Return value:
		perimeter(a): square perimeter
    '''
    return 4 * a
